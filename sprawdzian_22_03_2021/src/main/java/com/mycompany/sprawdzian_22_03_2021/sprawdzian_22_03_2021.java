    private void addKeyListenertoPartial(){
        ms_jTextFieldPartialNumber.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                char ch = e.getKeyChar();
                    if(ch == KeyEvent.VK_BACK_SPACE || ch >= '0' && ch <= '9' || ch == KeyEvent.VK_PERIOD){
                        ms_jTextFieldPartialNumber.setEditable(true);
                    }else{
                    ms_jTextFieldPartialNumber.setEditable(false);
                }
            }

            @Override
            public void keyPressed(KeyEvent e) {
                
            }

            @Override
            public void keyReleased(KeyEvent e) {
                
            }
        });
    }
    private void zapisz(){
        try{
            FileWriter fw = new FileWriter(file, true);
            fw.write(ms_jTextFieldPartialNumber.getText()+" - "+ms_jTextFieldModel.getText());
            fw.close();
        }catch (IOException ex) {
            System.out.println("Blad: "+ex.toString());
        }
    }
